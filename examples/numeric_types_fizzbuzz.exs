# FizzBuzz խնդիրը
#
# Պայմանը․ տպել 1-ից 100 թվերը, բայց՝
#   - եթե թիվը բաժանվում է 3-ի՝ թվի փոխարեն տպել "Fizz"
#   - եթե թիվը բաժանվում է 5-ի՝ տպել "Buzz"
#   - եթե թիվը բաժանվում է և՛ 3-ի, և՛ 5-ի՝ տպել "FizzBuzz"
#
# Գործարկելու համար տերմինալում մուտքագրեք՝
#   elixir numeric_types_fizzbuzz.exs

defmodule FizzBuzz do
  def convert(number) do
    cond do
      # Եթե թիվը բաժանվում է և՛ 3-ի, և՛ 5-ի, ապա այն բաժանվում է 15-ի։
      # Այս ստուգումը պետք է առաջինը լինի, հակառակ դեպքում 15-ի
      # բազմապատիկների համար կտպվի "Fizz"՝ "FizzBuzz"-ի փոխարեն։
      rem(number, 15) == 0 -> "FizzBuzz"
      # rem(number, 3) == 0 նշանակում է՝ 3-ի բաժանելիս մնացորդը 0 է,
      # այսինքն՝ թիվը բաժանվում է 3-ի առանց մնացորդի։
      rem(number, 3) == 0 -> "Fizz"
      rem(number, 5) == 0 -> "Buzz"
      # Ոչ մի պայման չբավարարվեց․ վերադարձնում ենք թիվը՝ որպես տեքստ։
      true -> Integer.to_string(number)
    end
  end
end

# Այս տողը ֆունկցիան կանչում է 1-ից 100 բոլոր թվերի համար
# և տպում արդյունքը։ Այս գրելաձևին կծանոթանանք ավելի ուշ։
Enum.each(1..100, fn number -> IO.puts(FizzBuzz.convert(number)) end)
